package com.rigadev.nutricapps.model;

public class MyPlateModel {
    String idPlate, type, foodOrderID, idUser, name, portion, calories, datePlate;

    public MyPlateModel() {
    }

    public String getIdPlate() {
        return idPlate;
    }

    public void setIdPlate(String idPlate) {
        this.idPlate = idPlate;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getFoodOrderID() {
        return foodOrderID;
    }

    public void setFoodOrderID(String foodOrderID) {
        this.foodOrderID = foodOrderID;
    }

    public String getIdUser() {
        return idUser;
    }

    public void setIdUser(String idUser) {
        this.idUser = idUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPortion() {
        return portion;
    }

    public void setPortion(String portion) {
        this.portion = portion;
    }

    public String getCalories() {
        return calories;
    }

    public void setCalories(String calories) {
        this.calories = calories;
    }

    public String getDatePlate() {
        return datePlate;
    }

    public void setDatePlate(String datePlate) {
        this.datePlate = datePlate;
    }
}
