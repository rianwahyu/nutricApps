package com.rigadev.nutricapps.model;

public class NotifikasiModel {
    String idNotifikasi, namaNotifikasi, contentNotifikasi, dateNotifikasi, readNotifikasi;

    public NotifikasiModel() {
    }

    public String getIdNotifikasi() {
        return idNotifikasi;
    }

    public void setIdNotifikasi(String idNotifikasi) {
        this.idNotifikasi = idNotifikasi;
    }

    public String getNamaNotifikasi() {
        return namaNotifikasi;
    }

    public void setNamaNotifikasi(String namaNotifikasi) {
        this.namaNotifikasi = namaNotifikasi;
    }

    public String getContentNotifikasi() {
        return contentNotifikasi;
    }

    public void setContentNotifikasi(String contentNotifikasi) {
        this.contentNotifikasi = contentNotifikasi;
    }

    public String getDateNotifikasi() {
        return dateNotifikasi;
    }

    public void setDateNotifikasi(String dateNotifikasi) {
        this.dateNotifikasi = dateNotifikasi;
    }

    public String getReadNotifikasi() {
        return readNotifikasi;
    }

    public void setReadNotifikasi(String readNotifikasi) {
        this.readNotifikasi = readNotifikasi;
    }
}
